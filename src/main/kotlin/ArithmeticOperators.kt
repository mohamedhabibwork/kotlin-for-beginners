fun main() {
    var num1 = 10
    val num2 = 20

    println("num1 + num2 = ${num1 + num2}")

    println("num1 - num2 = ${num1 - num2}")

    println("num1 * num2 = ${num1 * num2}")

    println("num1 / num2 = ${num1 / num2}")

    println("num1 % num2 = ${num1 % num2}")

    println("........................................")
    println("num1 + num2 = ${num1.plus(num2)}")
    println("num1 - num2 = ${num1.minus(num2)}")
    println("num1 * num2 = ${num1.times(num2)}")
    println("num1 / num2 = ${num1.div(num2)}")
    println("num1 % num2 = ${num1.rem(num2)}")


}
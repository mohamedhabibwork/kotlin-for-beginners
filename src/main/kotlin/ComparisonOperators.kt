fun main() {
    val n1 = 10
    val n2 = 23
    val result = n1 > n2
    println("result = $result")

    val result2 = n1 < n2
    println("result2 = $result2")

    val result3 = n1 >= n2
    println("result3 = $result3")

    val result4 = n1 <= n2
    println("result4 = $result4")

    val result5 = n1 == n2
    println("result5 = $result5")
}
fun main() {
    // example of val
    val name = "John"
    val age = 30
    val department = "IT"

    println("Name: $name")
    println("Age: $age")
    println("Department: $department")

    // example of var
    var salary = 1000f
    println("Salary: $salary")

    salary = 2000f
    println("Salary: $salary")

}